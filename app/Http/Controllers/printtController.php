    function exportt_pdf(Request $request){
        $awal = $request->awal;
        $akhir = $request->akhir;

        $data = tenan::WhereBetween('created_at',[$awal, $akhir])->get();
        $exportt = PDF::loadview('admin.printt.exportt_pdf',['data'=>$data]);

        return $exportt->stream('laporan.pdf');
    }

